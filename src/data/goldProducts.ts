// Helper function to get brokers by product type
export function getBrokersByProduct(productType: GoldProduct['type']): Broker[] {
  return BROKERS.filter(broker => broker.products.includes(productType))
    .sort((a, b) => b.rating - a.rating);
}

// Helper function to get product by type
export function getProductByType(type: GoldProduct['type']): GoldProduct | undefined {
  return GOLD_PRODUCTS.find(p => p.type === type);
}
